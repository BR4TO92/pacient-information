package com.oncochain.model;

public class Pacient {

	private String name;
	private int age;

	public Pacient() {};

	public Pacient(String name, int age) {
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
}
